delete_site_flow() {
  section "Delete Site"
  validate_runtime
  local site confirm db_name delete_db archive_path="" db_dropped="no" hosts_removed="no" answer
  select_site site || return 0
  [[ -n "$site" ]] || return 0

  db_name="$(site_config_value "$site" db_name 2>/dev/null || true)"

  warn "this will archive site folder: $BENCH_DIR/sites/$site"
  if [[ -n "$db_name" ]]; then
    warn "detected site database: $db_name"
  else
    warn "could not read db_name from site_config.json; database will not be dropped automatically"
  fi
  read -r -p "Type the site name to delete: " confirm
  if [[ "$confirm" != "$site" ]]; then
    warn "delete cancelled"
    return 0
  fi

  read -r -p "Delete database and DB user too? Y/n: " delete_db
  case "${delete_db:-Y}" in
    y|Y|yes|YES)
      if [[ -n "$db_name" ]]; then
        if drop_database_and_user "$db_name"; then
          db_dropped="yes"
        else
          warn "database was not dropped; continuing with site archive"
        fi
      else
        warn "database was not dropped because db_name is unavailable"
      fi
      ;;
    *)
      warn "database and DB user kept"
      ;;
  esac

  archive_site_folder "$site" archive_path || die "could not archive site folder: $site"

  read -r -p "Remove /etc/hosts entry? y/N: " answer
  case "${answer:-}" in
    y|Y|yes|YES)
      if remove_hosts_entry "$site"; then
        hosts_removed="yes"
      fi
      ;;
    *)
      warn "hosts entry kept"
      ;;
  esac

  append_note_secret "Site deleted / archived" \
    "Site: $site" \
    "DB name: ${db_name:-unknown}" \
    "DB dropped: $db_dropped" \
    "Hosts entry removed: $hosts_removed" \
    "Archive path: $archive_path"

  remove_site_secret "$site"
  ok "site archived: $archive_path"
}

