rename_site_to_lowercase_flow() {
  section "Rename Site to Lowercase"
  validate_runtime

  local old_site new_site confirm
  select_uppercase_site old_site || return 0
  [[ -n "$old_site" ]] || return 0
  new_site="${old_site,,}"

  if [[ -e "$BENCH_DIR/sites/$new_site" ]]; then
    die "lowercase target already exists: $BENCH_DIR/sites/$new_site"
  fi

  warn "Bench should be stopped before renaming a site folder."
  warn "This will rename: $old_site -> $new_site"
  warn "The database name will not be changed."
  read -r -p "Type '$old_site -> $new_site' to confirm: " confirm
  if [[ "$confirm" != "$old_site -> $new_site" ]]; then
    warn "rename cancelled"
    return 0
  fi

  info "[RUN] mv $BENCH_DIR/sites/$old_site $BENCH_DIR/sites/$new_site"
  mv "$BENCH_DIR/sites/$old_site" "$BENCH_DIR/sites/$new_site"
  ensure_hosts_entry "$new_site"

  append_note_secret "Site renamed to lowercase" \
    "Old site: $old_site" \
    "New site: $new_site" \
    "Database name changed: no"

  if ! bench_run --site "$new_site" list-apps; then
    warn "bench could not read apps after rename. If needed, manually revert:"
    warn "  mv '$BENCH_DIR/sites/$new_site' '$BENCH_DIR/sites/$old_site'"
  fi

  ok "site renamed: $old_site -> $new_site"
}

